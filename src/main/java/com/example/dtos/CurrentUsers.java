package com.example.dtos;

public class CurrentUsers {
	private Long id;
	private String username;
	private String image;
	private Long currentMovieHallId;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public Long getCurrentMovieHallId() {
		return currentMovieHallId;
	}
	public void setCurrentMovieHallId(Long currentMovieHallId) {
		this.currentMovieHallId = currentMovieHallId;
	}
	
	
	
	
	
}
